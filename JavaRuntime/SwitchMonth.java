package com.raystech.JavaRuntime;

public class SwitchMonth {
	public static void main(String[] args) {
		String month = "February";
		switch (month) {
		case "January":
			System.out.println("first month of year");
			break;
		case "February":
			System.out.println("second month of year");
			break;
		case "march":
			System.out.println("third minth of year");
			break;
		case "april":
			System.out.println("forth month of year");
			break;
		default:
			System.out.println("month is comming");
			break;
		}
		
		
		}
	}


